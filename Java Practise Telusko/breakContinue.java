
public class breakContinue {

	public static void main(String args[])
	{
		for(int i=0; i<10; i++)
		{
			if(i==7)
			{
				break; //it will break the loop and no more will be printed
				//continue will print everything in loop except the number stated in if
			}
		System.out.println(i);
		}
		
	}
}
